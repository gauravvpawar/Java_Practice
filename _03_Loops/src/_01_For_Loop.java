public class _01_For_Loop
{
    public static void main(String[] args) {
        /*
        for loop based on
        for(initialise; condition ; increment/ decrement)
        {
                // if condition is true then execute the block
        }
         */

        for(int i = 1;i<=10;i++)
        {
            System.out.println("hello " + i);
        }
    }
}
