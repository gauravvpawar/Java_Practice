public class _02_While_Loop
{
    public static void main(String[] args) {
        /*
         while loop is same work for the iteration but when user do not know how much time he have to
         iterate then he can use the while loop
         */

        int count = 0;
        while(count <= 10)
        {
            System.out.println("Count : " + count);
            count++;
        }
    }
}
