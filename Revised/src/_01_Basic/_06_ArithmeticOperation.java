package _01_Basic;

public class _06_ArithmeticOperation
{
    public static void main(String[] args)
    {
        // Arithmetic operation we perform between two number
        // it include number means operand
        // operator it include + - * / %

        int a = 10;
        int b = 20;

        System.out.println(a + b); // addition 30
        System.out.println(a - b); // subtract -10
        System.out.println(a * b);//multiply 200
        System.out.println(a / b);// divide -> 0
        System.out.println(a % b);//modulo it return remainder 10
    }
}
